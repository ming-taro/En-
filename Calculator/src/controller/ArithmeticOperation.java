package controller;

public class ArithmeticOperation {
	public double divideNumber(double firstValue, double secondValue) {
		return firstValue / secondValue;
	}
	public double multiplyNumber(double firstValue, double secondValue) {
		return firstValue * secondValue;
	}
	public double subtractNumber(double firstValue, double secondValue) {
		return firstValue - secondValue;
	}
	public double addNumber(double firstValue, double secondValue) {
		return firstValue + secondValue;
	}
}
